#!/usr/bin/env python3

"""Stage 2: Run eval inference.

Loads eval prompts from stage 1, composes runtime context (system prompt,
user summary, memory context, tools), runs the model under test, and
records full traces as EvalTrace JSONL.

Supports resume: prompt IDs already present in the output file are skipped.

Usage:
    python evals/run_eval_inference.py \
        --prompts-dir output/evals/prompts/ \
        --output-dir output/evals/traces/ \
        --backend gemini \
        --model gemini-3-flash
"""

import argparse
import json
from collections.abc import Callable
from pathlib import Path
from typing import Any

from rich.progress import Progress

from datagen.user_simulator import simulate_user_turn
from pkna.eval.types import EvalPrompt, EvalTrace
from pkna.inference.memory import MemoryBank
from pkna.inference.system_prompts import (
    SUITE_TEMPLATE_MAP,
    prepend_context_to_messages,
    render_system_prompt,
)
from pkna.inference.tools import make_eval_tools
from pkna.llm.backends import LLMBackend, create_backend
from pkna.logging import setup_logging

console, log = setup_logging()


def load_prompts(prompts_dir: Path, suites: list[str] | None) -> list[EvalPrompt]:
    """Load eval prompts from JSONL files in the prompts directory."""
    prompts: list[EvalPrompt] = []
    for path in sorted(prompts_dir.glob("*.jsonl")):
        suite_name = path.stem
        if suites and suite_name not in suites:
            continue
        with open(path, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    prompts.append(EvalPrompt.model_validate_json(line))
    return prompts


def load_completed_ids(output_path: Path) -> set[str]:
    """Scan an existing output JSONL for prompt IDs already processed."""
    completed: set[str] = set()
    if not output_path.exists():
        return completed
    with open(output_path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                data = json.loads(line)
                completed.add(data["prompt_id"])
            except (json.JSONDecodeError, KeyError):
                continue
    return completed


def load_memory_bank(bank_path: str) -> MemoryBank | None:
    """Load a memory bank from a file path, or return None if empty/missing."""
    if not bank_path:
        return None
    p = Path(bank_path)
    if not p.exists():
        log.warning(f"Memory bank not found: {p}")
        return None
    return MemoryBank.load(p)


def compose_context(
    prompt: EvalPrompt,
) -> str:
    """Build the static system prompt for a given eval prompt."""
    template = SUITE_TEMPLATE_MAP.get(prompt.suite, "full")
    return render_system_prompt(template=template)


def run_single_turn(
    prompt: EvalPrompt,
    backend: LLMBackend,
    model_name: str,
) -> EvalTrace | None:
    """Run inference for a single-turn eval prompt and return the trace."""
    system_prompt = compose_context(prompt)
    messages = prepend_context_to_messages(
        prompt.messages, prompt.user_summary, prompt.memory_context
    )
    bank = load_memory_bank(prompt.memory_bank_path)
    tools = make_eval_tools(prompt.tools, memory_bank=bank, eval_mode=True)

    result = backend.generate(
        system=system_prompt,
        messages=messages,
        tools=tools if tools else None,
    )
    if result is None:
        log.error(f"Inference failed for prompt {prompt.id}")
        return None

    all_messages: list[dict[str, Any]] = list(messages)
    if result.messages:
        all_messages.extend(result.messages)
    else:
        all_messages.append({"role": "assistant", "content": result.text})

    return EvalTrace(
        prompt_id=prompt.id,
        suite=prompt.suite,
        model=model_name,
        messages=all_messages,
        tool_calls=result.tool_calls,
        thinking=result.thinking,
    )


def _visible_messages(messages: list[dict[str, Any]]) -> list[dict[str, str]]:
    """Extract user/assistant messages for the user simulator (skip tool turns)."""
    return [
        {"role": m["role"], "content": m["content"]}
        for m in messages
        if m["role"] in ("user", "assistant") and m.get("content")
    ]


def _get_directive(directives: list[str], turn_index: int) -> str:
    """Get the directive for a given turn, cycling if the list is short."""
    if not directives:
        return "continue"
    return directives[turn_index % len(directives)]


def run_multi_turn(
    prompt: EvalPrompt,
    backend: LLMBackend,
    model_name: str,
    simulator_backend: LLMBackend | None = None,
) -> EvalTrace | None:
    """Run a multi-turn conversation for stability evaluation.

    The first user message comes from the prompt. Subsequent user messages
    are generated by the user simulator, guided by per-turn directives
    from prompt.metadata["directives"].

    Args:
        prompt: Eval prompt with metadata keys "turn_count" and "directives".
        backend: LLM backend for the model under test.
        model_name: Name of the model under test (for the trace).
        simulator_backend: LLM backend for the user simulator. Defaults to
            the same backend as the model under test.
    """
    sim_backend = simulator_backend or backend
    turn_count: int = prompt.metadata.get("turn_count", 10)
    directives: list[str] = prompt.metadata.get("directives", [])

    system_prompt = compose_context(prompt)
    messages = prepend_context_to_messages(
        prompt.messages, prompt.user_summary, prompt.memory_context
    )
    bank = load_memory_bank(prompt.memory_bank_path)
    tools = make_eval_tools(prompt.tools, memory_bank=bank, eval_mode=True)
    tools_or_none: list[Callable[..., str]] | None = tools if tools else None

    all_messages: list[dict[str, Any]] = list(messages)
    all_tool_calls: list[dict[str, Any]] = []
    all_thinking: list[str] = []

    for turn in range(turn_count):
        result = backend.generate(
            system=system_prompt,
            messages=all_messages,
            tools=tools_or_none,
        )
        if result is None:
            log.error(f"Inference failed for prompt {prompt.id} at turn {turn}")
            break

        if result.messages:
            all_messages.extend(result.messages)
        else:
            all_messages.append({"role": "assistant", "content": result.text})
        all_tool_calls.extend(result.tool_calls)
        if result.thinking:
            all_thinking.append(result.thinking)

        if turn >= turn_count - 1:
            break

        directive = _get_directive(directives, turn)
        user_msg = simulate_user_turn(
            backend=sim_backend,
            conversation=_visible_messages(all_messages),
            user_profile=prompt.user_summary,
            directive=directive,
        )
        if user_msg is None:
            log.error(f"User simulator failed for prompt {prompt.id} at turn {turn}")
            break
        all_messages.append({"role": "user", "content": user_msg})

    if not any(m["role"] == "assistant" for m in all_messages):
        return None

    combined_thinking = "\n".join(all_thinking) if all_thinking else None
    return EvalTrace(
        prompt_id=prompt.id,
        suite=prompt.suite,
        model=model_name,
        messages=all_messages,
        tool_calls=all_tool_calls,
        thinking=combined_thinking,
    )


def run_single_prompt(
    prompt: EvalPrompt,
    backend: LLMBackend,
    model_name: str,
    simulator_backend: LLMBackend | None = None,
) -> EvalTrace | None:
    """Run inference for an eval prompt, dispatching single- or multi-turn."""
    if prompt.metadata.get("multi_turn"):
        return run_multi_turn(prompt, backend, model_name, simulator_backend)
    return run_single_turn(prompt, backend, model_name)


def run_eval(
    prompts: list[EvalPrompt],
    backend: LLMBackend,
    model_name: str,
    output_dir: Path,
) -> int:
    """Run inference for all prompts, writing traces to per-suite JSONL files.

    Returns the number of traces written.
    """
    output_dir.mkdir(parents=True, exist_ok=True)

    suites_in_run = sorted({p.suite for p in prompts})
    completed_by_suite: dict[str, set[str]] = {}
    for suite in suites_in_run:
        output_path = output_dir / f"{suite}.jsonl"
        completed_by_suite[suite] = load_completed_ids(output_path)

    pending = [p for p in prompts if p.id not in completed_by_suite.get(p.suite, set())]

    if not pending:
        log.info("All prompts already processed. Nothing to do.")
        return 0

    skipped = len(prompts) - len(pending)
    if skipped > 0:
        log.info(f"Resuming: skipping {skipped} already-processed prompts")

    written = 0
    file_handles: dict[str, Any] = {}

    try:
        with Progress(console=console) as progress:
            task = progress.add_task("Running inference", total=len(pending))

            for prompt in pending:
                trace = run_single_prompt(prompt, backend, model_name)
                if trace is None:
                    progress.advance(task)
                    continue

                suite = prompt.suite
                if suite not in file_handles:
                    output_path = output_dir / f"{suite}.jsonl"
                    file_handles[suite] = open(output_path, "a", encoding="utf-8")

                file_handles[suite].write(trace.model_dump_json() + "\n")
                file_handles[suite].flush()
                written += 1
                progress.advance(task)
    finally:
        for fh in file_handles.values():
            fh.close()

    return written


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Run eval inference (stage 2 of the eval pipeline)"
    )
    parser.add_argument(
        "--prompts-dir",
        type=Path,
        default=Path("output/evals/prompts"),
        help="Directory containing prompt JSONL files from stage 1",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("output/evals/traces"),
        help="Directory to write trace JSONL files",
    )
    parser.add_argument(
        "--backend",
        type=str,
        default="gemini",
        help="LLM backend to use (gemini or anthropic)",
    )
    parser.add_argument(
        "--model",
        type=str,
        default=None,
        help="Model name (defaults to backend's default)",
    )
    parser.add_argument(
        "--suites",
        type=str,
        default=None,
        help="Comma-separated list of suites to run (default: all)",
    )
    args = parser.parse_args()

    suite_filter = args.suites.split(",") if args.suites else None

    console.print("[bold cyan]Eval Inference Runner[/bold cyan]\n")

    prompts = load_prompts(args.prompts_dir, suite_filter)
    if not prompts:
        console.print("[yellow]No prompts found.[/yellow]")
        return

    log.info(
        f"Loaded {len(prompts)} prompts across {len({p.suite for p in prompts})} suites"
    )

    backend = create_backend(args.backend, args.model)
    model_name = args.model or args.backend

    written = run_eval(prompts, backend, model_name, args.output_dir)

    console.print(
        f"\n[bold green]Done.[/bold green] {written} traces written to {args.output_dir}"
    )


if __name__ == "__main__":
    main()
